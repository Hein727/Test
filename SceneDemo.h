#pragma once
#include "Scene.h"

class SceneDemo : public Scene
{
private:
    SceneDemo() = default;  // デフォルトコンストラクタ。private にして、外部からのインスタンス化を防ぐ。
    SceneDemo(const SceneDemo&) = delete;  // コピーコンストラクタを削除。インスタンスのコピーを防ぐ。
    SceneDemo& operator=(const SceneDemo&) = delete;  // 代入演算子を削除。インスタンスの代入を防ぐ。

public:
    static SceneDemo& getInstance()
    {
        static SceneDemo instance;
        return instance;
    }

    void init() override;
    void update() override;
    void draw() override;
};
